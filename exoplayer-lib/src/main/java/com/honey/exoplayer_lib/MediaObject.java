package com.honey.exoplayer_lib;

public class MediaObject {

    public String mediaUrl;

    public String mediaCoverImgUrl;

    public String getMediaUrl() {
        return mediaUrl;
    }

    public void setMediaUrl(String mediaUrl) {
        this.mediaUrl = mediaUrl;
    }

    public String getMediaCoverImgUrl() {
        return mediaCoverImgUrl;
    }

    public void setMediaCoverImgUrl(String mediaCoverImgUrl) {
        this.mediaCoverImgUrl = mediaCoverImgUrl;
    }
}
